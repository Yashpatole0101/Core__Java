package day3;

public class Assignmentop {

	public static void main(String[] args) {
		//assignmentop- (=,+=,-=,*=,/=)
		
		int a=10;
		
		a+=3;
		System.out.println(a);
		
     	a-=4;
		System.out.println(a);
		
		
		a*=4;
    	System.out.println(a);
    	
    	a/=2;
    	System.out.println(a);
    	
    	
    	a%=4;
    	System.out.println(a);
    	
    	
	}}