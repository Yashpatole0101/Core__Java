package day3;

public class Logical_or_not {

	public static void main(String[] args) {
		//OR(||) operator
		//t||f=>t
		//f||t=>t
		//t||t=>t
		//f||f=>f
		
		
		int a=19;
		int b=67;
		
		System.out.println(a>=b||b==a);
		System.out.println((23==56)||(23>4));
		
		//logical NOT(!)
		//!true=>galse
		//!false=>true
		
		
		System.out.println(!(a<b));
	
				

	}

}
