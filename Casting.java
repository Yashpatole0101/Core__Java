package day3;

public class Casting {

	public static void main(String[] args) {
		//explicit casting
		double a=234.88;
		int d=(int)a;
		System.out.println(a);
		System.out.println(d);
		
		
		//implicit casting
		int i=34;
		double q=i;
		System.out.println(i);
		System.out.println(q);
		

	}

}
